#include "libft.h"

void	*ft_memchr(const void *ptr, int val, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)ptr)[i] == (unsigned char)val)
			return ((void *)&((unsigned char *)ptr)[i]);
		i += 1;
	}
	return (NULL);
}
